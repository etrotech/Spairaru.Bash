#!/usr/bin/env bash
# ######################################################################################
# TERMINAL CORE UTILITY
# ######################################################################################
# ======================================================================================
# GET TERMINAL COLUMNS
# ======================================================================================
s_gtTmlRowx() {


	stty \
		size \
	| cut \
		-d ' ' \
		-f1 \
	;


} ;



# ======================================================================================
# GET TERMINAL COLUMNS
# ======================================================================================
s_gtTmlColx() {


	stty \
		size \
	| cut \
		-d ' ' \
		-f2 \
	;


} ;
